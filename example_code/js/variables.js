/* var vs let vs const */
// var is function scoped
// let and const are block scoped
var aVar = 10;
let aLet = 10;
const aConst = 10;

// Declare variables
let name = "John"; // String
let age = 25; // Number
let isStudent = true; // Boolean
let hobbies = ["reading", "coding", "gaming"]; // Array
let address = {
    street: "123 Main St",
    city: "New York",
    state: "NY"
}; // Object
let x; // Undefined
let y = null; // Null
