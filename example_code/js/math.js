let x = 100;
let y = 2;

// Addition
console.log(x + y); // 102

// Subtraction
console.log(x - y); // 98

// Multiplication
console.log(x * y); // 200

// Division
console.log(x / y); // 50

// Modulus
console.log(x % y); // 0

// Exponentiation
console.log(x ** y); // 10000

// Increment
x++;

// Decrement
y--;
